# Object Oriented Programming 

class Dog() :

    def bark(self) :
        print("Aw aw aw")


d = Dog()

d.bark()


class Calculator() :

    def __init__(self) -> None:
        pass

    def add_(self, x) : # * Parameter x 

        return x + 4 
    


add = Calculator()
print(add.add_(4)) # * argument number 4 
